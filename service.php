	<script>
		const itemsPerPage = 6;
		const serviceBlocks = document.querySelectorAll('.service-block');
		const totalServices = serviceBlocks.length;
		const totalPages = Math.ceil(totalServices / itemsPerPage);
		let currentPage = 1;

		function showPage(pageNumber) {
			const start = (pageNumber - 1) * itemsPerPage;
			const end = start + itemsPerPage;

			serviceBlocks.forEach((service, index) => {
				service.style.display = (index >= start && index < end) ? 'block' : 'none';
			});
		}

		function updatePagination() {
			const paginationContainer = document.getElementById('pagination');
			paginationContainer.innerHTML = '';

			const prevPage = document.createElement('li');
			prevPage.classList.add('page-item');
			prevPage.id = 'prevPage';

			const prevLink = document.createElement('a');
			prevLink.classList.add('page-link');
			prevLink.href = 'javascript:void(0);';
			prevLink.setAttribute('aria-label', 'Previous');
			prevLink.innerHTML = '<span aria-hidden="true">&laquo;</span>';

			prevLink.addEventListener('click', function() {
				if (currentPage > 1) {
					currentPage--;
					showPage(currentPage);
				}
			});

			prevPage.appendChild(prevLink);
			paginationContainer.appendChild(prevPage);

			for (let i = 1; i <= totalPages; i++) {
				const pageItem = document.createElement('li');
				pageItem.classList.add('page-item');

				const pageLink = document.createElement('a');
				pageLink.classList.add('page-link');
				pageLink.textContent = i;

				pageLink.addEventListener('click', (function(pageNumber) {
					return function() {
						currentPage = pageNumber;
						showPage(currentPage);
					};
				})(i));

				pageItem.appendChild(pageLink);
				paginationContainer.appendChild(pageItem);
			}

			const nextPage = document.createElement('li');
			nextPage.classList.add('page-item');
			nextPage.id = 'nextPage';

			const nextLink = document.createElement('a');
			nextLink.classList.add('page-link');
			nextLink.href = 'javascript:void(0);';
			nextLink.setAttribute('aria-label', 'Next');
			nextLink.innerHTML = '<span aria-hidden="true">&raquo;</span>';

			nextLink.addEventListener('click', function() {
				if (currentPage < totalPages) {
					currentPage++;
					showPage(currentPage);
				}
			});

			nextPage.appendChild(nextLink);
			paginationContainer.appendChild(nextPage);
		}

		showPage(currentPage);
		updatePagination();
	</script>

